    private void getData(){
        
        try {
        
            ObjectInputStream read = new ObjectInputStream(new FileInputStream(lecturerDir));
            Object o; // an object of Object class (The parent of all objects)
            
            try{
                o = read.readObject();
                if(o != null)
                AllLecturer = (ArrayList<Lecturer>) o;
            }
            catch(IOException | ClassNotFoundException c){
                c.printStackTrace();
                
            }
            
            read.close();
        }
        
        catch(IOException outer){
            outer.printStackTrace();
            
        }
        
        
        
        
        
    
    }
    
    private void listLecturers(){
    
        
        lecturerModel.setRowCount(0); // reset the entire table
        
        for(int i = 0 ; i < AllLecturer.size(); i++){
            
            Object[] info = {AllLecturer.get(i).getNic(), AllLecturer.get(i).getEmail() , AllLecturer.get(i).getSalary() , AllLecturer.get(i).getAddress(), AllLecturer.get(i).getMobile(),  AllLecturer.get(i).getLectureHours(), AllLecturer.get(i).getSubjectID() };
        
            lecturerModel.addRow(info);
                
        }
        
        foundIndex = -1; // reset search result with update
        
    
    }
    
    

