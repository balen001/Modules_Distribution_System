    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        
        
        DefaultTableModel tableModel = (DefaultTableModel) lecturerTbl.getModel();
        
        if(foundIndex == -1){
        //to delete the row
        if (lecturerTbl.getSelectedRowCount() == 1){
        
            // if only a single row selected perform delete
            
            int r = lecturerTbl.getSelectedRow();
            
            tableModel.removeRow(r);
            AllLecturer.remove(r);
        
        }
        
        else if (lecturerTbl.getSelectedRowCount() == 0) {
        
            // if the table empty (no data) then dislay a message
           
            JOptionPane.showMessageDialog(null, "Nothing is selected for delete");
        }
        
        else {
        // if row is not selected or multiple row selected
            JOptionPane.showMessageDialog(null, "Please select a single row for delete");
        }
        }
        
        
        
        
        //to delete data after search
        else {
            if (lecturerTbl.getSelectedRowCount() == 1){
        
            // if only a single row selected perform delete
            int selectedRow = lecturerTbl.getSelectedRow();
            
            tableModel.removeRow(selectedRow);
            AllLecturer.remove(foundIndex);
        
        }
        
        else if (lecturerTbl.getSelectedRowCount() == 0) {
        
            // if the table empty (no data) then dislay a message
           
            JOptionPane.showMessageDialog(null, "The table is empty");
        }
        
        else {
        // if row is not selected or multiple row selected
            JOptionPane.showMessageDialog(null, "Please select a single row for delete");
        }
            
        }
        
        
        
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        //write into file permenantly
        
        try {
            FileOutputStream fileOutput = new FileOutputStream(lecturerDir);
            ObjectOutputStream write = new ObjectOutputStream(fileOutput); 
		
            ArrayList <Lecturer> editedStudent = new ArrayList <Lecturer>();
            editedStudent = AllLecturer;
            
            write.writeObject(editedStudent);
            write.close();
            JOptionPane.showMessageDialog(null, "Saved successfuly");
       	      
            //write.close();
        } catch (IOException e) {
            
            JOptionPane.showMessageDialog(null, "error");
	}
        
        getData();
        listLecturers();
    }//GEN-LAST:event_saveBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
       
        String searchName = searchTxt.getText();
        foundIndex = -1;
        
        
        if (!searchName.isEmpty()){
        for(int j = 0; j < AllLecturer.size(); j++){
        
                if(AllLecturer.get(j).getNic().equalsIgnoreCase(searchName)){
                    
                    JOptionPane.showMessageDialog(null, "Found!!");
                    foundIndex = j;
                
            }
        
        }
        
        if (foundIndex == -1){
            JOptionPane.showMessageDialog(null, "Not found");
        }
        
        if (foundIndex != -1)
        {
        lecturerModel.setRowCount(0); // reset the entire table
        Object[] info = {AllLecturer.get(foundIndex).getNic(), AllLecturer.get(foundIndex).getEmail() , AllLecturer.get(foundIndex).getSalary() , AllLecturer.get(foundIndex).getAddress(), AllLecturer.get(foundIndex).getMobile(),  AllLecturer.get(foundIndex).getLectureHours(), AllLecturer.get(foundIndex).getSubjectID() };
        lecturerModel.addRow(info);
        
        }
        }
        
        else{listLecturers();}
           
    }//GEN-LAST:event_searchBtnActionPerformed

