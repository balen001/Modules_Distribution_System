    private void getComboList(){
        ArrayList <Course> AllCourses = new ArrayList <Course>();
        
        try{
            ObjectInputStream read = new ObjectInputStream(new FileInputStream(courseDir));

            Object o;
            
           
            try{
                o = read.readObject();
                if(o != null)
                AllCourses = (ArrayList<Course>) o;
            }
            catch(IOException | ClassNotFoundException c){
                c.printStackTrace();
                System.out.println("Here");
            }
            
				
	read.close();
        
        for(int i = 0; i < AllCourses.size(); i++){
            String option = AllCourses.get(i).getCourseName()  + ", " + AllCourses.get(i).getCourseId();
            subjectCombo.addItem(option);
        }
	
        
        }
        catch(IOException e){
            e.printStackTrace();
        }
        
        
        
    }
    
