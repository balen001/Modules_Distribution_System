    private void addSubjectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSubjectBtnActionPerformed
        
        
        String subjectName = subNameTxt.getText();
        String subjectID = subIDTxt.getText();
        int subjectCredit = 0;
        String comboText = subjectCombo.getSelectedItem().toString();
        String [] splitted = comboText.split("\\s*,\\s*");
        String CourseID = splitted[1];
        
        
        boolean auth = true;  // if any error happens auth will be false otherwise its true
        
        
        try{
        subjectCredit = Integer.parseInt(subCreditTxt.getText());
        }
        catch (NumberFormatException e){          // We have studied number format exception in class
            JOptionPane.showMessageDialog(null, "Invalid credit number!!");
            auth = false;
        }
        
        
        //check for empty fields
        if(subjectName.isEmpty()){
            JOptionPane.showMessageDialog(null, "Subject name is empty");
            auth = false;
        }
        if(subjectID.isEmpty()){
            JOptionPane.showMessageDialog(null, "Subject ID is empty");
            auth = false;
        }
        if(CourseID.isEmpty()){
            JOptionPane.showMessageDialog(null, "Course ID is not selected");
            auth = false;
        }
        
        
        Subject newSubject = new Subject(subjectName, subjectCredit, subjectID, CourseID);
        
        ArrayList <Subject> AllSubjects = new ArrayList <Subject>();
        
        
        // To read all available data first
        if(auth){
        try {
        
            ObjectInputStream read = new ObjectInputStream(new FileInputStream(fileDir));
            Object o; // an object of Object class (The parent of all objects)
            
            try{
                o = read.readObject();
                if(o != null)
                AllSubjects = (ArrayList<Subject>) o;
            }
            catch(IOException | ClassNotFoundException c){
                c.printStackTrace();
                
            }
            
            read.close();
        }
        
        catch(IOException outer){
            outer.printStackTrace();
            
        }
        
        
        
        
        // to write the data
        
        try {
            FileOutputStream fileOutput = new FileOutputStream(fileDir);
            ObjectOutputStream write = new ObjectOutputStream(fileOutput); 
		
            ArrayList <Subject> addSubject = new ArrayList <Subject>();
            addSubject = AllSubjects;
            addSubject.add(newSubject);
            write.writeObject(addSubject);
            write.close();
            JOptionPane.showMessageDialog(null, "Subject Successfully added");
       	      
            write.close();
        } catch (IOException e) {
            
            JOptionPane.showMessageDialog(null, "error");
	}
        
        }
        
        
        clearFields();
    }//GEN-LAST:event_addSubjectBtnActionPerformed

